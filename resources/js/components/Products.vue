import axios from "axios";
import Pagination from "./Pagination.vue";
import Product from "./Product.vue"
import Multiselect from 'vue-multiselect'
import 'vue-multiselect/dist/vue-multiselect.min.css'
import SearchInput from "./SearchInput.vue";

export default {
    components: {
        Pagination,
        Product,
        Multiselect,
        SearchInput
    },
    data() {
        return {
            products: [],
            filters: [],
            activeFilters: {},
            currentPage: 1,
            limit: 12,
            sort_by: "",
        };
    },

    mounted() {
        this.getProducts();
        this.getFilters();
    },



    watch: {
        activeFilters: {
            handler() {
                this.getProducts();
            },
            deep: true,
            immediate: true,
        },
        currentPage: {
            handler() {
                this.getProducts();
            }
        },
        limit: {
            handler(newVal, oldVal) {
                if (newVal !== oldVal) {
                    this.getProducts();
                }
            }
        }
    },

    methods: {
        onPageChange(page) {
            console.log(page)
            this.currentPage = page;
        },
        getProducts() {
            const params = {
                page: this.currentPage,
                limit: this.limit,
                sort_by: this.sort_by,
                ...this.serializeFilters(),
            };

            axios
                .get("/api/catalog/products", { params })
                .then((response) => {
                    console.log(response.data);
                    this.products = response.data;
                })
                .catch((error) => {
                    console.error(error);
                });
        },

        getFilters() {
            const params = { ...this.serializeFilters() };
            axios
                .get("/api/catalog/filters", { params })
                .then((response) => {
                    this.filters = response.data;

                    this.activeFilters = Object.fromEntries(
                        this.filters.map(f => [f.slug, []])
                    );

                })
                .catch((error) => {
                    console.error("Error fetching filters:", error);
                });
        },
        serializeFilters() {
            const params = {};

            Object.entries(this.activeFilters).forEach(([key, values]) => {
                values.forEach((value, i) => {
                    params[`filter[${key}][${i}]`] = value;
                });
            });

            return params;
        },

        handleUpdateFilters({ param, values }) {
            console.log(param, values)
        },
    },
};
